#include <stdio.h>
/**
 * main - entry point
 *
 * Function_name - printf
 *
 * Return: 0 = Success
 */
int main(void)
{
	int n;
	/* Loop to print single digits of base 10 */
	for (n = 0; n <= 9; n++)
	{
		/* Print numbers */
		printf("%d", n);
	}
	{
		/* Print a new line */
		printf("\n");
	}
	return (0);
}
