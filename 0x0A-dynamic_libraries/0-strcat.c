/**
 **_strcat - creates a new string by combining two
 *
 *@dest: string destination
 *
 *@src:  string source
 *
 *Return: 0 = Success
 */
char *_strcat(char *dest, char *src)
{
  char d;
  char s;

  for (d = 0; d < s && s != '\0'; d++)
    for (s = 0; s > d && d != '\0'; s++)
      {
	while (d < s)
	  dest = '\0';
	d++;
      }
  {
    return (dest);
  }
  {
    return (src);
  }
  {
    return (0);
  }
}
