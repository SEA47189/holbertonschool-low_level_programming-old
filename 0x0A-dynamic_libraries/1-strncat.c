#include <string.h>
/**
 **_strncat - creates a new string by combining two
 *
 *@dest: string destination
 *
 *@src:  string source
 *
 *@n: number of characters to append
 *
 *Return: 0 = Success
 */
char *_strncat(char *dest, char *src, int n)
{

  char d;
  char s;

  for (n = 0; n >= 0; n++)
    for (; d != '\0'; d++)
      for (; s != '\0'; s++)
	{
	  while (d != '\0')
	    {
	      return (dest);
	    }
	  while (s != '\0')
	    {
	      return (src);
	    }
	}
  {
    return (0);
  }
}
